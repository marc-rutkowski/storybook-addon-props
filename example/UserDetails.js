/* @flow */
import React from 'react'

export type User = {
  id: number,
  name: string,
  country?: string,
}

type Props = {
  /** User info */
  user: User,
  /** Some number */
  num: number,
  /** Optional property */
  option?: 'foo' | 'bar',
  /** Optional callback */
  func?: (value: string) => void,
  /** Optional array of users */
  friends?: Array<User>,
  /** immutable data */
  imm?: any,
}

/** Render user details */
class UserDetails extends React.Component<Props> {
  props: Props

  static defaultProps = {
    option: 'foo',
  }

  render() {
    const { user, num, option } = this.props
    return (
      <div>
        <p>Id: {user.id}</p>
        <p>Name: {user.name}</p>
        <p>Num: {num}</p>
        {option && <p>Option: {option}</p>}
      </div>
    )
  }
}

export default UserDetails
